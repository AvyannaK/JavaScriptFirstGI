
let num1 = 40
let num2 = 10

function addition(n1,n2){
    return n1 + n2;
}

function subtraction(n1,n2){
    return n1 - n2;
}

function multiplication(n1,n2){
    return n1 * n2;
}

function division(n1,n2){
    return n1 / n2;
}

console.log(addition(num1,num2));
console.log(subtraction(num1,num2));
